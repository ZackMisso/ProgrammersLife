package player;

public class BaseStats {
	private int intelligence;
	private int creativity;
	private int personality;
	private int ambition;
	private int luck;
	private int physique;
	private int enthusiasm;
	
	// getter methods
	public int getIntelligence(){return intelligence;}
	public int getCreativity(){return creativity;}
	public int getPersonality(){return personality;}
	public int getAmbition(){return ambition;}
	public int getLuck(){return luck;}
	public int getPhysique(){return physique;}
	public int getEnthusiasm(){return enthusiasm;}
	
	// setter methods
	public void setIntelligence(int param){intelligence = param;}
	public void setCreativity(int param){creativity = param;}
	public void setPersonality(int param){personality = param;}
	public void setAmbition(int param){ambition = param;}
	public void setLuck(int param){luck = param;}
	public void setPhysique(int param){physique = param;}
	public void setEnthusiasm(int param){enthusiasm = param;}
}
